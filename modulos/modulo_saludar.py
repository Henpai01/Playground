
def saludar(name):
    return f"Hola {name}, como estas?"

def saludar_formal(name):
    return f"Hola Buneos dias, tardes o noches mi estimado {name}, como se encuentra el dia de hoy?"

def saludar_raro(name):
    return f"Hola {name},que mas mi negro?"

def saludar_venezolano(name):
    return f"que mas {name},chamo como estas?"